# Division of odd numbers:

# Use the floor method to have whole numbers, use // :

num1 = 11
num2 = 5

print("This uses floor division to cut out any decimals: ", num1//num2)
print("And this is the standard division, returns a float: ", num1/num2)

# Use Modulo operator to get the remainder

print("Using MODULO, we get the remainder of the division of 11/5: ", num1 % num2)

# POWER Operator:
power_total = num1 ** 2
print("This is using the POWER operator, making 11 ** 2 :", power_total)